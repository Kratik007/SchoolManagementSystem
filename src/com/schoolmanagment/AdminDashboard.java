    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
            String name=name1.getText();
            String qualific=qualification.getText();
            String mob=mobile.getText();
            String fathusn=fathusname.getText();
            String address=address1.getText();
            String pass=password.getText();
            if(name.equals("")){
                JOptionPane.showMessageDialog(this,"Enter Name");
                name1.requestFocus();
            }else if(qualific.equals("")){
                JOptionPane.showMessageDialog(this,"Enter Qualification");
                qualification.requestFocus();
            }else if(mob.equals("")){
                JOptionPane.showMessageDialog(this,"Enter Mobile No.");
                mobile.requestFocus();
            }else if(fathusn.equals("")){
                JOptionPane.showMessageDialog(this, "Enter Father or Husband Name");
                fathusname.requestFocus();
            }else if(address.equals("")){
                JOptionPane.showMessageDialog(this,"Enter Address");
                address1.requestFocus();
            }else if(pass.equals("")){
                JOptionPane.showMessageDialog(this, "Enter password");
                password.requestFocus();
            }else if(dob1.getText()=="format yyyy/mm/dd"){
                JOptionPane.showMessageDialog(this,"Enter Valid Dob");
                dob1.requestFocus();
            }else if(pass.length()>10){
                JOptionPane.showMessageDialog(this,"Password can have upto 10 character");
            }
            else {
                com.Entities.Staff staff=new com.Entities.Staff(fathusn,new Date(dob1.getText()),qualific,mob,address,name);
                StaffDao.saveStaffDetails(staff, pass, this);
                JOptionPane.showMessageDialog(this,"Staff's Details Saved successfully");
                clearStaffForm();
            }
    }//GEN-LAST:event_jButton4ActionPerformed

