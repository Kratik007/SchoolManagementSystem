    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String stuname=name.getText();
//        Date date=;
        String f_name=fname.getText();
        String m_name=mname.getText();
        String p_mobile=pmobile.getText();
        String occup=occupation.getText();
        String aadh=aadhar.getText();
        String sssm=sssmid.getText();
        String addres=address.getText();
        if(stuname.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Student Name");
            name.requestFocus();
        }
        else if(f_name.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Student's Father Name");
            fname.requestFocus();
        }
        else if(m_name.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Student's Mother Name");
            mname.requestFocus();
        }
        else if(p_mobile.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Parents Mobile No.");
            pmobile.requestFocus();
        }
        else if(occup.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Father's Occupation");
            occupation.requestFocus();
        }
        else if(aadh.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Student's Aadhar No");
            aadhar.requestFocus();
        }
        else if(dob.getText().equals("format yyyy/mm/dd")){
            JOptionPane.showMessageDialog(this,"Enter valid dob");
            dob.requestFocus();
        }
        else if(addres.equals("")){
            JOptionPane.showMessageDialog(this,"Enter Parmanent Address");
            address.requestFocus();
        }else{
        Student student=new Student(stuname,new Date(dob.getText()),f_name,m_name,p_mobile,occup,aadh,sssm,addres);
        
        int enrollment=StudentDao.saveStudent(student,this);
        boolean bol2=ClassDao.setStudentClass(enrollment, (String)jComboBox2.getSelectedItem());
        if(enrollment==0){
            JOptionPane.showMessageDialog(this,"Error in saving student record");
        }else if(!bol2){
            boolean success=ClassDao.setStudentClass(enrollment, (String)jComboBox2.getSelectedItem());
            if(!success){
                JOptionPane.showMessageDialog(this,"Error in saving student");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Student Saved successfully");
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

